            <p class="dropcap dropcap-dark offset-top-34">Today I 
would like to talk about some interesting art forms connected with 
handmade craft. They represent various handmade ideas that you will 
certainly find beneficial for your handmade craft skills as well as for 
your free time. Handmade has always been a field where anyone who has 
manual skills and desire to develop them can achieve great results. 
Though handmade craft was not very popular until recent years, it has 
always been present in various genres and styles of art themselves. 
Today we see the rise of handmade popularity. People do not only use 
handmade art as a source of ideas for gifts, but also as a basis for 
business. That’s why our website is devoted to the most popular and 
innovative art nowadays.</p>
            <p>If you would like to discover the art of handmade 
gifts for yourself, the first step to make is to learn the basics of 
this art. As an experienced handmade artist, I recommend you to use 
various resources with handmade tutorials to assist you in grasping the 
secrets of handmade. My first advice is to get to know scrapbooking, or 
speaking naturally, methods of your photos and photo albums 
transformation.</p>
            <h4 class="text-regular">Scrapbooking</h4>
            <p>A scrapbook is simply a decorated photo album that 
also preserves the stories behind the photos (called journaling), it can
also hold memorabilia (tickets, certificates, letters, etc.). You might
have a family album as well as smaller theme albums for vacations, home
improvement or a baby’s first year. Some scrapbookers create an album 
for each family member, or even for their friends to make ordinary photo
albums look awesome.</p>
            <p class="big font-weight-bold offset-top-24">Handmade toys and accessories</p>
            <p>Making toys using your own imagination and hands has 
always been very popular. To my mind, even the most fastidious child 
will like a handmade toy, and it’s not only because it is made by 
his/her parent or relative. It is also a very original present, and none
of your child’s friends will have the toy that looks just the same.</p><img class="img-fluid offset-top-34" src="images/post-05-770x508.jpg" alt="" width="770" height="508">
            <p class="big font-weight-bold offset-top-34">Decorations and bijouterie</p>
            <p>The last but of course not the least on my list today
are decorations and bijouterie or costume jewelry. These types of 
handmade craft represent an amazing possibility to add something new to 
your home’s interior. The biggest advantage here is your potential 
saving in comparison with original accessories that are very expensive.</p>
